a = input('Digite algo: ')
print('O tipo de arquivo primitivo é ', type(a))

#verificar se a string tem espaços;
print('Só tem espaço? ',a.isspace())

#verificar se a string é númerico;
print('É um número? ',a.isnumeric())

#verificar se a string é alfabetico;
print('É alfabetico?', a.isalpha())

#verificar se a string é alfanumerico;
print('É letra e número? ', a.isalnum())

#verificar se a string é maiúscula;
print('Está em maiuscula? ', a.isupper())

#verificar se a string é minúscula;
print('Está em minuscula? ', a.islower())

#verificar se a string é capitalizado (maiúscula e minúscula);
print('Está capitalizada? ', a.istitle())




